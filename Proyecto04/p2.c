int max(int a,int b)/**Entre parentesis se colocan los par\'ametros 
                       formales de la funci\'on*/
{                   /**El cuerpo de la funci\'on se coloca en un bloque*/
  return ((a>b) ? a : b); /**Lo que est\'a colocado entre { y llave }*/
}                         /**recibe el nombre de bloque. */

